    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // Delete item from database
        df = (DefaultTableModel)jTable1.getModel();        
        int selected = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(df.getValueAt(selected, 0).toString());
        
        try {
            pst = conn.prepareStatement("DELETE FROM products WHERE id = ?");
            pst.setInt(1, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Product Deleted");

            jTextFieldProductName.setText("");
            jTextFieldProductDescription.setText("");
            jTextFieldProductBarcode.setText("");
            jTextFieldProductCost.setText("");
            jTextFieldProductRetail.setText("");
            jTextFieldProductQuantity.setText("");
            jTextFieldProductROLevel.setText("");
            jTextFieldProductName.requestFocus();

            load();
            jButtonAdd.setEnabled(true);     // Add button activated
            jButtonUpdate.setEnabled(false); // update button deactivated
            jButtonDelete.setEnabled(false); // delete button deactivated
        } catch (SQLException ex) {
            Logger.getLogger(Vendor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

