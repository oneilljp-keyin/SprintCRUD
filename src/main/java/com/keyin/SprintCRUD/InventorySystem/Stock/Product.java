    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // Add Button
        try {
            String productName        = jTextFieldProductName.getText();
            String productDescription = jTextFieldProductDescription.getText();
            String productBarcode     = jTextFieldProductBarcode.getText();
            String productCost        = jTextFieldProductCost.getText();
            String productRetail      = jTextFieldProductRetail.getText();
            String productQty         = jTextFieldProductQuantity.getText();
            String productROL         = jTextFieldProductROLevel.getText();
            
            pst = conn.prepareStatement("INSERT INTO products (name, description, barcode, costPrice, retailPrice, quantity, reOrderLevel) values (?,?,?,?,?,?,?)");
            pst.setString(1, productName);
            pst.setString(2, productDescription);
            pst.setString(3, productBarcode);
            pst.setString(4, productCost);
            pst.setString(5, productRetail);
            pst.setString(6, productQty);
            pst.setString(7, productROL);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Product Added");

            jTextFieldProductName.setText("");
            jTextFieldProductDescription.setText("");
            jTextFieldProductBarcode.setText("");
            jTextFieldProductCost.setText("");
            jTextFieldProductRetail.setText("");
            jTextFieldProductQuantity.setText("");
            jTextFieldProductROLevel.setText("");
            jTextFieldProductName.requestFocus();
            load();
        } catch (SQLException ex) {
            Logger.getLogger(Vendor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonAddActionPerformed

